//Write a Program to calculate and display the volume of a CUBE having its length (l = 10 cm), width (w =12 cm) and height (h = 8 cm).

#include<stdio.h>
int main(void)
{
    int l, w, h, vol;
    l =10;
    w =12;
    h =8;
    vol =l*w*h;
    printf("The Volume of the cube is: %d", vol);

    return 0;
}
